#slicing

#string
str='learning'
print(str[:])  #learning
print(str[2:5]) #arn

#list
lst=[1,2,'a','py',3]
print(lst[:3])  #[1,2,'a']
print(lst[2:])  #['a','py',3]
print(lst[1:-1]) #[2,'a','py']

#tuple
tup=(1,2,3,4,'ro')
print(tup[-3:-1]) #(3,4)
print(tup[-1:]) #('ro',)
print(tup[:-3]) #(1,2)

